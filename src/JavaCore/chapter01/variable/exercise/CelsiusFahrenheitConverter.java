package JavaCore.chapter01.variable.exercise;

public class CelsiusFahrenheitConverter {
    public static void main (String[] args) {

        double temperatureEnCelsius = 100;
        double temperatureEnFarenheit = (temperatureEnCelsius * 1.8) + 32 ;

        System.out.println(temperatureEnCelsius + "°C est égal à " + temperatureEnFarenheit + "F");

    }
    }
